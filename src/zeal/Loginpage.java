    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
      boolean bat=false;
       int user=Integer.parseInt(username.getText());
      String pass=password.getText();
      
       for(int i=0;i<ZealBankofIndia.obj.size();i++)
       {
           ZealBankofIndia.k=ZealBankofIndia.obj.get(i);
           if(user==ZealBankofIndia.k.getAcc() && pass.equals(ZealBankofIndia.k.getpass()))
           {
              bat=true;
               java.awt.EventQueue.invokeLater(() -> {
                   new Afterlogin().setVisible(true);
               });
               break;
           }
       }
           if(bat==false)
           {
               javax.swing.JOptionPane.showMessageDialog(null,"Invalid");
               ZealBankofIndia.k=null;
           }
       
    }//GEN-LAST:event_loginActionPerformed

